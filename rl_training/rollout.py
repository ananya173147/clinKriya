"""
Rollout collection for GRPO training.

Runs complete episodes using the policy model and environment, collecting
trajectories with per-turn log probabilities for policy gradient updates.

Key concepts:
  - A Trajectory is one complete episode: a sequence of (state, action, reward) tuples
  - A RolloutGroup is K trajectories for the same task (for GRPO advantage computation)
  - collect_rollout() runs one episode end-to-end
  - collect_group_rollouts() runs K episodes for GRPO
"""

